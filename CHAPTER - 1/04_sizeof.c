#include <stdio.h>

int main()
{
    /* 
    To find out the Size of Bytes through this "sizeof" function
    and know how much your computer take for a variable
    */
    printf("Size of int: %lu bytes\n", sizeof(int));
    printf("Size of char: %lu byte\n", sizeof(char));
    printf("Size of float: %lu bytes\n", sizeof(float));

    return 0;
}
